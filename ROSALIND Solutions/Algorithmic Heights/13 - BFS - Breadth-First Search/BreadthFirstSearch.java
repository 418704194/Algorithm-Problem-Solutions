  public static int[] distanceBFS( String[] g ) {
    int[][] gInt = new int[g.length][2];
    int max = 0;
    for(int i = 0; i < g.length; ++i) {
      String[] parts = g[i].split(" ");
      gInt[i][0] = Integer.parseInt(parts[0]);
      gInt[i][1] = Integer.parseInt(parts[1]);
      if(gInt[i][0] > max) {
        max = gInt[i][0];
      }
      if(gInt[i][1] > max) {
        max = gInt[i][1];
      }
    }
    ArrayList<ArrayList<Integer>> graph = new ArrayList<>();
    for(int i = 0; i <= max; ++i) { // graph[0] is dummy
      graph.add(new ArrayList<>());
    }
    for(int i = 0; i < gInt.length; ++i) {
      graph.get(gInt[i][0]).add(gInt[i][1]);
    }
    
    // perform BFS
    int[] distances = new int[max];
    for(int i = 1; i < distances.length; ++i) {
      distances[i] = Integer.MAX_VALUE;
    }
    boolean[] visited = new boolean[max];
    LinkedList<Integer> q1 = new LinkedList<>();
    q1.add(1);
    LinkedList<Integer> q2 = new LinkedList<>(); // distances
    q2.add(0);
    while(!q1.isEmpty()) {
      int curr = q1.removeFirst();
      int d = q2.removeFirst();
      visited[curr-1] = true;
      if(distances[curr-1] > d) {
        distances[curr-1] = d;
      }
      ArrayList<Integer> n = graph.get(curr);
      for(int i = 0; i < n.size(); ++i) {
        if(!visited[n.get(i)-1]) {
          q1.addLast(n.get(i));
          q2.addLast(d+1);
        }
      }
    }
    for(int i = 0; i < visited.length; ++i) {
      if(!visited[i]) {
        distances[i] = -1;
      }
    }
    return distances;
  }
